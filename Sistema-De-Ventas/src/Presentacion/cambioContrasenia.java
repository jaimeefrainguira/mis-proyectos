    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        // TODO add your handling code here:
        if(!jTextField2.getText().equals(""))
       {
           if(jTextField4.getText().equals(jTextField2.getText()))
            {
                dos=true;
                validar.colorVerde(jTextField4);
                validar.colorVerde(jTextField2);
                if(uno&&dos)
                    jButton2.setEnabled(dos);
            }
            else
            {
                dos=false;
                validar.colorRojo(jTextField2);
            validar.colorRojo(jTextField4);
                jButton2.setEnabled(false);
            }
       }
    }//GEN-LAST:event_jTextField4KeyReleased

    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        if(jTextField4.getText().equals(jTextField2.getText()))
        {
            dos=true;
            validar.colorVerde(jTextField4);
            validar.colorVerde(jTextField2);
            if(uno&&dos)
                jButton2.setEnabled(dos);
        }
        else
        {
            dos=false;
            validar.colorRojo(jTextField2);
            validar.colorRojo(jTextField4);
            jButton2.setEnabled(false);
        }
    }//GEN-LAST:event_jTextField2KeyReleased

    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        if(usuario.getUSU_CLAVE().equals(jTextField1.getText()))
        {
            validar.colorVerde(jTextField1);            
            uno=true;
            if(uno&&dos)
                jButton2.setEnabled(dos);
        }
        else
        {
            uno=false;
            validar.colorRojo(jTextField1);
            jButton2.setEnabled(false);
        }
    }//GEN-LAST:event_jTextField1KeyReleased

