    private void bt_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardarActionPerformed
        // TODO add your handling code here:
        int id=0;
        String sexoUsuario = null;
        boolean estado=true;
        if(radioBt_Masculino.isSelected())
        sexoUsuario="M";

        if(radioBt_Femenino.isSelected())
        sexoUsuario="F";

        if(radioBt_Activo.isSelected())
        estado=true;

        if(radioBt_Inactivo.isSelected())
        estado=false;

        int año1 =fechingreso.getCalendar().get(Calendar.YEAR);
        int mes1=fechingreso.getCalendar().get(Calendar.MARCH);
        int dia1=fechingreso.getCalendar().get(Calendar.DAY_OF_MONTH);
        String fechaIngreso=String.valueOf(año1+"-"+mes1+"-"+dia1);

        cliente = new
        Proveedor
        (
            id,
            txtCedula.getText(),
            txtNombre.getText(),
            txtDireccion.getText(),
            txtTelefono.getText(),
            txtCorreo.getText(),
            sexoUsuario,

            fechaIngreso,
            txt_observaciones.getText(),
            estado
        );

        if(txtCodigo.getText().equals(""))
        {
            controladorProveedor.registrar(cliente);
        }
        else
        {
            cliente.setPER_ID(Integer.parseInt(txtCodigo.getText()));
            controladorProveedor.actualizar(cliente);
        }
        this.dispose();

//        tabla.setModel(controladorProveedor.getAll());
//        PanelConPestania.setSelectedIndex(0);
//        controladorProveedor.anchoTablas(tabla);
    }//GEN-LAST:event_bt_guardarActionPerformed

